package JavaConcepts;

public class IncrementDecrement {

	public static void main(String[] args) {
		
		int a=10;
		int b=a++;//b=a+1;
		System.out.println(b);//post increment
		
		System.out.println("********");
		int a1=10;
		int b1=++a1;
		System.out.println(b1);// preincrement
		
		
		System.out.println("********");
		int x=10;
		int y=x--;
		System.out.println(y);//post decrement
		
		System.out.println("********");
		int m=10;
		int n=--m;
		System.out.println(n);// predecrmentsd
		

	}

}
