package JavaConcepts;

public class DataTypes {

	public static void main(String[] args) {
	
		//main() method is the starting execution point of program
		
		int i=10;//positive value
		int j=30;
		int k=-10;//negative value
		int l=0;//zero
		
		
		//double
		double d=23.33;
		double d1=-98.00;
		double d2=100;//treated as 100.00*
		
		//char
		char c='a';
		char c1='A';
		char c2='1';
		char c3='$';
		//char c='aa';//(not allowed only single digit inside the quotes)
		
		//boolean
		boolean b=true;
		boolean b1=false;//keywords and only allow true or false
		
		
		//string-is not a datatype and it is class and it starts with capital letter
		
		String S="Selenium";
		String s1=" Hi! This is my first java program";
		String s3="2000";
		String s4="45.89";
		
		//Primitive data types :int double char boolean 
		
		System.out.println(j);
		System.out.println(k);//syso ctrl+spc
		
		System.out.println(j+k);//simple addition
		
		
	}

}
